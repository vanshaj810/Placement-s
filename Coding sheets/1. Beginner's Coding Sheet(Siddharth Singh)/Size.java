public class Size {
    public static void main(String[] args) {
        System.out.println("Integer's Size is "+(Integer.SIZE/8)+" Bytes");
        System.out.println("Float's Size is "+(Float.SIZE/8)+" Bytes");
        System.out.println("Double's Size is "+(Double.SIZE/8)+" Bytes");
        System.out.println("Char's Size is "+(Character.SIZE/8)+" Bytes");
        System.out.println("Char's Size is "+(Long.SIZE/8)+" Bytes");
    }
}
